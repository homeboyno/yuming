        <h3 class="question">杨某团伙金融诈骗洗钱案</h3>
        <p>2006年6月，杨JF犯罪团伙金融诈骗四川省某烟草公司人民币6500 万元。杨某在明知杨JF等人所转6500万元资金系诈骗犯罪所得的情况 下，为掩饰犯罪所得的来源和性质，仍提供四川蜀港投资有限公司(虚 假注册成立)和四川富阳投资有限公司账户，并帮助杨JF 等人转款、取 现用于投资水电站、加油站、矿 山、房地产、证券等项目，清洗犯 罪所得。杨某的洗钱数额是目前我 国洗钱数额最大的洗钱罪判例，也 是四川省首例洗钱罪判例。2008 年8月15目，成都市中级人民法院对杨JF 、 杨某等人一审公开宣判，认定杨某犯洗钱罪，判 处有期徒刑八年，并处罚金500万元。</p>
<h3 class="question">汪某投资企业洗钱案</h3>
<p>广州市海珠区人民法院于2004年3月审理并判决了国内首宗洗钱 罪案件。被告汪某于2001年底结识同类人区某，知道区某长期在加拿 大、香港及广东省从事毒品贩卖活动。2002年上半年，区某对汪某表 示为其子女考虑，今后想从事正当职业，想把资金（贩卖毒品所得）带 回境内。汪某当即为其出谋划策，采用购买企业经营方式来处理毒资。 2002年8月，区某将毒资折港币约600万元从加拿大带到香港，由香港 入关，汪某开车到深圳接应，带回广州。汪某通过广州某律师事务所 以区某的520万港元（折合550万元人民币），购得广州木业公司董事 长，每月领取5000元以上的工资，负责处理公司对外联络事宜，还收 取区某赠送的一辆奔驰小汽车。区某接管公司后，开始经营木材生意， 利润为20%。区某采用虚设盈亏损账目，用于掩饰、隐瞒其违法所得的来源与性质，意图将毒品犯罪的违法所得转为合法收益。2003年3月区 某贩毒案告破，区某、汪某被依法逮捕。经法院审理认定，汪某犯洗钱 罪，依照《刑法》第一百九十一条第（五）项、第六十五条、第二十七 条的规定判处被告人汪某有期徒刑一年六个月。</p>
